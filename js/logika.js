let a = true;
let b = false;
let c = true;
let d = false;

// OR( || )
// OR akan bernilai true jika salah satu true
console.log(d || b || a);

// AND ( && )
// AND akan bernilai false jika salah satu false
console.log(a && b && c);

// Pembanding
let x = 8;
let y = 7;

// Lebih besar
console.log(x > y); // apakah x lebih besar dari y

// lebih kecil
console.log(x < y); // apakah x lebih kecil dari y

// Sama dengan
console.log(x == y); //apakah x sama dengan y

// Lebih besar sama dengan
console.log(x >= y); // apakah x lebih besar sama dengan y

// Lebih kecil sama dengan
console.log(x <= y); // apakah x lebih kecil sama dengan y

let lima = '5';
let limo = 5;

// Identik( === ) = sama tipedata dan isinya
console.log(lima === limo);
